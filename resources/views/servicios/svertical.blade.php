<h3 class="heading2">Señales Verticales</h3>
<p>
     Se caracterizan por estar instaladas de pie. Están compuestas por la señalización preventiva,
    reglamentaria, transitoria e informativa. Tienen la tarea de informar, restringir o prevenir
     diferentes situaciones a los usuarios que transitan por vías públicas con el fin de garantizar
     su seguridad y en general, el orden del tránsito por vías locales y nacionales.
</p>

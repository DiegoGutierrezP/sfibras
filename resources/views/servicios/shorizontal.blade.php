<h3 class="heading2">Señales Horizontal</h3>
<p>
    Se caracterizan por estar pintadas en el suelo y tienen la misión de orientar,
     organizar y regular el tránsito: Flechas, delimitaciones viales, ceda el paso,
     etc. En algunos casos, son usadas para complementar las órdenes o advertencias
     de otros dispositivos, tales como las señales verticales y semáforos; en otros,
      transmiten instrucciones que no pueden ser presentadas mediante el uso de ningún otro
      dispositivo, siendo un modo muy efectivo de hacerlas legibles para los usuarios.
</p>
